class Example:

    def hello():
        print('Hello')

    def instance_hello(self):
        print(f'instance hello {self}')

    @staticmethod
    def static_hello():
        print('static hello')

    @classmethod
    def class_hello(cls):
        print(f'instance hello {cls}')


Example.hello()
p = Example()
p.instance_hello()
Example.static_hello()
p.static_hello()
Example.class_hello()
p.class_hello()
